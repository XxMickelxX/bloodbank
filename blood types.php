        <div class="text"> <h1>Blood Components</h1>
    <p>There are four basic components that comprise human blood: plasma, red blood cells, white blood cells and platelets.</p>
   <h2> Red Blood Cells</h2>

<p>Red blood cells represent 40%-45% of your blood volume. They are generated from your bone marrow at a rate of four to five billion per hour. They have a lifecycle of about 120 days in the body.</p>

<h2>Platelets</h2>

<p>Platelets are an amazing part of your blood. Platelets are the smallest of our blood cells and literally look like small plates in their non-active form. Platelets control bleeding. Wherever a wound occurs, the blood vessel will send out a signal. Platelets receive that signal and travel to the area and transform into their “active” formation, growing long tentacles to make contact with the vessel and form clusters to plug the wound until it heals.</p>

<h2>Plasma</h2>

<p> is the liquid portion of your blood. Plasma is yellowish in color and is made up mostly of water, but it also contains proteins, sugars, hormones and salts. It transports water and nutrients to your body’s tissues.</p>

<h2 >Blood Cells</h2>

<p>Although white blood cells (leukocytes) only account for about 1% of your blood, they are very important. White blood cells are essential for good health and protection against illness and disease. Like red blood cells, they are constantly being generated from your bone marrow. They flow through the bloodstream and attack foreign bodies, like viruses and bacteria. They can even leave the bloodstream to extend the fight into tissue.</p></div>
   

